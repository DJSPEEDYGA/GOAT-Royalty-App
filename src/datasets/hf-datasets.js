  // ─── PREVIEW DATA (NO AUTH) ─────────────────────
  async previewData(id, filename, rows = 10) {
    return new Promise((resolve) => {
      // Use the rows API for parquet files
      const url = `${this.baseURL}/api/datasets/${id}/parquet/default/train`;
      const directUrl = `${this.baseURL}/datasets/${id}/resolve/main/${filename || 'README.md'}`;
      
      // Try direct file download for small files with full redirect support
      const fetchWithRedirects = (fetchUrl, depth = 0) => {
        if (depth > 10) { resolve({ success: false, error: 'Too many redirects' }); return; }
        const client = fetchUrl.startsWith('https') ? https : http;
        client.get(fetchUrl, { 
          headers: { 'User-Agent': 'GOAT-Datasets-App/1.0' } 
        }, (res) => {
          if ([301, 302, 303, 307, 308].includes(res.statusCode) && res.headers.location) {
            let nextUrl = res.headers.location;
            if (nextUrl.startsWith('/')) {
              const parsed = new URL(fetchUrl);
              nextUrl = `${parsed.protocol}//${parsed.host}${nextUrl}`;
            }
            fetchWithRedirects(nextUrl, depth + 1);
            return;
          }
          let data = '';
          res.on('data', chunk => { data += chunk; if (data.length > 12000) res.destroy(); });
          res.on('end', () => {
            resolve({ success: true, content: data.substring(0, 10000), truncated: data.length > 10000, filename });
          });
          res.on('error', (e) => resolve({ success: false, error: e.message }));
        }).on('error', (e) => resolve({ success: false, error: e.message }));
      };
      fetchWithRedirects(directUrl);
    });
  }

  // ─── DOWNLOAD DATASET FILE (NO AUTH) ────────────
  async downloadFile(datasetId, filename, onProgress) {
    const url = `${this.baseURL}/datasets/${datasetId}/resolve/main/${filename}`;
    const datasetDir = path.join(this.downloadDir, datasetId.replace('/', '__'));
    
    if (!fs.existsSync(datasetDir)) {
      fs.mkdirSync(datasetDir, { recursive: true });
    }
    
    const filePath = path.join(datasetDir, filename);
    const fileDir = path.dirname(filePath);
    if (!fs.existsSync(fileDir)) {
      fs.mkdirSync(fileDir, { recursive: true });
    }

    const downloadId = `${datasetId}/${filename}`;
    this.downloads.set(downloadId, { status: 'downloading', progress: 0, filename });
    this.emit('download-start', { id: downloadId, datasetId, filename });

    return new Promise((resolve, reject) => {
      const makeRequest = (requestUrl, redirectCount = 0) => {
        if (redirectCount > 10) {
          this.downloads.delete(downloadId);
          resolve({ success: false, error: 'Too many redirects' });
          return;
        }
        const client = requestUrl.startsWith('https') ? https : http;
        client.get(requestUrl, { headers: { 'User-Agent': 'GOAT-Datasets-App/1.0' } }, (res) => {
          // Handle ALL redirect types (301, 302, 303, 307, 308)
          if ([301, 302, 303, 307, 308].includes(res.statusCode) && res.headers.location) {
            let nextUrl = res.headers.location;
            // Handle relative redirects
            if (nextUrl.startsWith('/')) {
              const parsed = new URL(requestUrl);
              nextUrl = `${parsed.protocol}//${parsed.host}${nextUrl}`;
            }
            makeRequest(nextUrl, redirectCount + 1);
            return;
          }

          if (res.statusCode !== 200) {
            this.downloads.delete(downloadId);
            this.emit('download-error', { id: downloadId, error: `HTTP ${res.statusCode}` });
            resolve({ success: false, error: `HTTP ${res.statusCode}` });
            return;
          }

          const totalSize = parseInt(res.headers['content-length'] || '0', 10);
          let downloadedSize = 0;
          const fileStream = fs.createWriteStream(filePath);

          res.on('data', (chunk) => {
            downloadedSize += chunk.length;
            const progress = totalSize > 0 ? Math.round((downloadedSize / totalSize) * 100) : 0;
            this.downloads.set(downloadId, { 
              status: 'downloading', 
              progress, 
              filename,
              downloaded: downloadedSize,
              total: totalSize,
              downloadedFormatted: this._formatBytes(downloadedSize),
              totalFormatted: this._formatBytes(totalSize)
            });
            this.emit('download-progress', { id: downloadId, progress, downloaded: downloadedSize, total: totalSize });
            if (onProgress) onProgress(progress, downloadedSize, totalSize);
          });

          res.pipe(fileStream);

          fileStream.on('finish', () => {
            fileStream.close();
            this.downloads.delete(downloadId);
            
            // Register in local datasets
            this._registerLocal(datasetId, filePath, downloadedSize);
            
            this.emit('download-complete', { id: downloadId, path: filePath, size: downloadedSize });
            resolve({ success: true, path: filePath, size: downloadedSize, sizeFormatted: this._formatBytes(downloadedSize) });
          });

          fileStream.on('error', (err) => {
            this.downloads.delete(downloadId);
            fs.unlink(filePath, () => {});
            this.emit('download-error', { id: downloadId, error: err.message });
            resolve({ success: false, error: err.message });
          });
        }).on('error', (err) => {
          this.downloads.delete(downloadId);
          this.emit('download-error', { id: downloadId, error: err.message });
          resolve({ success: false, error: err.message });
        });
      };

      makeRequest(url);
    });
  }

  // ─── DOWNLOAD README (ALWAYS FREE) ─────────────
